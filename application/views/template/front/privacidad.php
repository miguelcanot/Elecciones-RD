Recolectamos la información básica de su perfil, principalmente el nombre, el género y el ID de Facebook para poder obtener la foto de perfil que se usan en ciertos tests.
 

Que hacemos con la información que recolectamos?

Nosotros requerimos esta información para entender sus necesidades y mejorar la experiencia de uso con la aplicación y particularmente por las razones que se muestran a continuación:

Recolectamos la información básica para crear fotos entretenidas para posteriormente poder ser compartidas con sus amigos en Facebook.
Adicionalmente pedimos cierta información, como por ejemplo acceso al News Feed de Facebook (cantidad de "Me Gusta", comentarios, posts, estados, u otros) o al Inbox (cantidad de mensajes) para sacar cálculos sobre la interacción del usuario con sus amigos y así poder entregar resultados más creibles y a la vez más entretenidos.
 

Como protegemos su información?

Nosotros garantizamos y nos aseguramos de la mejor forma posible que su información está segura. El uso de la información es temporaria mientras se procesan los resultados de los tests.

Nunca guardamos ninguna información sensible o privada del usuario, como passwords, mensajes privados, comentarios, gustos, etc.
Nunca guardamos información acerca de sus amigos en Facebook.
 

Revelamos la información a terceros?

Nosotros no vendemos, intercambiamos o transferimos de ninguna manera su información a terceros. 
LindoTest no se hace responsable por alguna actividad ilegal en Facebook. Nosotros no subimos ninguna información a Facebook. Al utilizar este sitio usted esta asumiendo el cumplimiento de nuestras políticas.

 

